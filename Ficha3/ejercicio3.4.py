#Mostrar por pantalla luego ordenados en
#forma alfabética.
nombre1=input("Escribe el primer nombre: ")
nombre2=input("Escribe el segundo nombre: ")
if nombre1 < nombre2:
     print(f"Primero va el {nombre1} y luego el {nombre2}")
else:
     print(f"Primero va el {nombre2} y luego el {nombre1}")


