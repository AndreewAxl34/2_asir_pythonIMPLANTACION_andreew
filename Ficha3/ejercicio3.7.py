#7.- Inicializar un string con la cadena "mAriA"
#luego llamar a sus métodos upper(), lower() y
#capitalize(), guardar los datos retornados en otros
#string y mostrarlos por pantalla.
nombre="mAriA"
mayusculas=nombre.upper()
minusculas=nombre.lower()
Capitalize=nombre.capitalize()
print(f"Mostraremos la cadena {nombre} de 3 maneras: \n Primero en MAYUSCULAS: {mayusculas},\n luego en minusculas: {minusculas} \n y en Capitalize: {Capitalize}")
